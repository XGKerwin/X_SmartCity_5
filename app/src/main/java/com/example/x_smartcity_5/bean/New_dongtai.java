public class New_dongtai {
    private int image;
    private String msg;

    public New_dongtai(int image, String msg) {
        this.image = image;
        this.msg = msg;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
